
package metodo.congruencial.mixto;


public class Variables {
    
    Double x0,b,d,Ui,Ni;

    public Variables(Double x0, Double b, Double d, Double Ui, Double Ni) {
        this.x0 = x0;
        this.b = b;
        this.d = d;
        this.Ui = Ui;
        this.Ni = Ni;
    }

    public Double getX0() {
        return x0;
    }

    public void setX0(Double x0) {
        this.x0 = x0;
    }

    public Double getB() {
        return b;
    }

    public void setB(Double b) {
        this.b = b;
    }

    public Double getD() {
        return d;
    }

    public void setD(Double d) {
        this.d = d;
    }

    public Double getUi() {
        return Ui;
    }

    public void setUi(Double Ui) {
        this.Ui = Ui;
    }

    public Double getNi() {
        return Ni;
    }

    public void setNi(Double Ni) {
        this.Ni = Ni;
    }

    
    

    
    
}
